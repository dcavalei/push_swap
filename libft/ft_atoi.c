#include "libft.h"

int	ft_atoi(const char *str)
{
	int	holder;
	int	sign;

	holder = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		holder = holder * 10 + *str - '0';
		str++;
	}
	return (holder * sign);
}
